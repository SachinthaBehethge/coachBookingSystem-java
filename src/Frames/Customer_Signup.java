    private void savecus(String firstname, String surename, String email, String password, String DOB, String telephone, String address, int gender) {

        //Connection dbcon = DBConnection.CreateDBConnection();
        //(String firstname, String surename, String email, String password, Date DOB, String telephone, String address, int gender)
        Connection dbcon;
        dbcon = DBConnection.CreateDBConnection();
        //query
        if (dbcon != null) {

            try {
                int addedcount = dbcon.createStatement().executeUpdate("insert into customer(FirstName, LastName, EmailAddress, Password, DOB, Gender, Telephone, Address)values('"+firstname+"', '"+surename+"', '"+email+"', '"+password+"', '"+DOB+"', '"+gender+"', '"+telephone+"', '"+address+"')");
                
                // + "values('" + firstname + "', '" + surename + "', '" + email + "', '" + password + "', '" + gender + "', '" + telephone + "', '" + address + "')");

                if (addedcount > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Customer successfully registered", "Save Success",
                            JOptionPane.INFORMATION_MESSAGE);
                     
                    CoachManmagementSystem signup = new CoachManmagementSystem();
                     signup.setVisible(true);
                     this.setVisible(false);
                     
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Customer registration failed", "Save Error",
                            JOptionPane.ERROR_MESSAGE);
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Customer registration failed", "Save Error",
                        JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Customer registration failed", "Save Error",
                    JOptionPane.ERROR_MESSAGE);

        }

    }
