    private void Signup1BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Signup1BTNActionPerformed
        // read input values
        String firstname = TxtFirstname.getText();
        String surename = TxtSurename.getText();
        String email = TxtEmail.getText();
        String password = TxtCustomerPw.getText();
        String conpassword = TxtCustomerPw2.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String DOB = dateFormat.format(TxtDOB.getDate());
        
        
        String telephone = TxtTPnum.getText();
        String address = TxtAddress.getText();

        if (RadioMale.isSelected()) {
            gender = 0;
        } else if (RadioFemale.isSelected()) {
            gender = 1;
        } else if (RadioOther.isSelected()) {
            gender = 2;
        }

        //checking pw match
        if (password.equals(conpassword)) {
            
            
          savecus(firstname, surename, email, password, DOB, telephone, address, gender);
            //savecus(firstname, surename, email, password, DOB, telephone, address, gender);

        } else {
            JOptionPane.showMessageDialog(rootPane, "Passwords do not match", "Save Error",
                    JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_Signup1BTNActionPerformed

