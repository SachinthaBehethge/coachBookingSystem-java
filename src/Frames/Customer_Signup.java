    private void TxtCustomerPwKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCustomerPwKeyReleased
        // TODO add your handling code here:
        int Cpasslength = TxtCustomerPw.getText().length();

        if (Cpasslength <= 3) {
            lblTooShort.setVisible(true);
            lblWeak.setVisible(false);
            lblMedium.setVisible(false);
            lblStrong.setVisible(false);

        } else if (Cpasslength >= 4 && Cpasslength <= 6) {
            lblTooShort.setVisible(false);
            lblWeak.setVisible(true);
            lblMedium.setVisible(false);
            lblStrong.setVisible(false);

        } else if (Cpasslength >= 7 && Cpasslength <= 8) {
            lblTooShort.setVisible(false);
            lblWeak.setVisible(false);
            lblMedium.setVisible(true);
            lblStrong.setVisible(false);

        } else if (Cpasslength >= 9 && Cpasslength <= 12) {
            lblTooShort.setVisible(false);
            lblWeak.setVisible(false);
            lblMedium.setVisible(false);
            lblStrong.setVisible(true);

        }


    }//GEN-LAST:event_TxtCustomerPwKeyReleased

    private void TxtCustomerPw2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCustomerPw2KeyReleased
        // TODO add your handling code here:
        int Cpasslength2 = TxtCustomerPw2.getText().length();

        if (TxtCustomerPw.getText().equals(TxtCustomerPw2.getText())) {

            lblRetype.setVisible(false);
            lblWrong.setVisible(false);
            lblConfirmed.setVisible(true);
        } else {
            lblRetype.setVisible(false);
            lblWrong.setVisible(true);
            lblConfirmed.setVisible(false);
        }

        if (Cpasslength2 == 0) {
            lblRetype.setVisible(true);
            lblWrong.setVisible(false);

        } else if (Cpasslength2 > 0) {
            lblRetype.setVisible(false);
        }


    }//GEN-LAST:event_TxtCustomerPw2KeyReleased

